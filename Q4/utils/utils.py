import numpy as np 

def rain_sample(root, path_zout):
    Ith, Ath = 35, 256
    # max_dbz = 0
    for zout in path_zout:
        path = root + '/' + zout
        data = np.load(path)
        Aev = np.sum(data > Ith)
        if Aev >= Ath:
            return True
    
    return False

def csi(truth, prediction):
    map_ = truth > 0
    truth = truth[map_]
    prediction = prediction[map_]
    prediction = prediction > 35
    truth = truth > 35
    n11, n10, n01, n00 = 0,0,0,0
    for i in range(len(truth)):
        if truth[i] and prediction[i]:
            n11 += 1
        elif truth[i] and not prediction[i]:
            n10 += 1
        elif not truth[i] and prediction[i]:
            n01 += 1
        else:
            n00 += 1
    assert n00+n01+n10+n11 == len(truth)
    return n11, n10, n01, n00



